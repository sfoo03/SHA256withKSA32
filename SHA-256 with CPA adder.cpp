WORD CSAsum(WORD A, WORD B, WORD C_In)
{
	WORD Sum;
	// Calculating value of sum
	Sum = C_In ^ (A ^ B);
	return Sum;
}

WORD CSAcout(WORD A, WORD B, WORD C_In)
{
	WORD C_Out;
	//Calculating value of C-Out
	C_Out = (A & B) | (B & C_In) | (A & C_In);
	C_Out = (C_Out << 1);
	return C_Out;

}

